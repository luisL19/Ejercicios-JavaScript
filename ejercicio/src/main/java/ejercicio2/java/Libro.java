package ejercicio2.java;

public class Libro {
    String titulo;
    String autor;
    String ISBN;
    int anoPublicacion;

    public Libro(String titulo, String autor, String ISBN, int anoPublicacion) {
        this.titulo = titulo;
        this.autor = autor;
        this.ISBN = ISBN;
        this.anoPublicacion = anoPublicacion;
    }
}
